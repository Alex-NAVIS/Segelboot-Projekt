// Globale Variable für die aktuelle Sprache mit sicherem Fallback
let currentLang = localStorage.getItem('selectedLang') || navigator.language.slice(0, 2) || 'de';

// Falls die ermittelte Sprache (z.B. des Browsers) nicht existiert, wechsle zu Deutsch
if (typeof translations === 'undefined' || !translations[currentLang]) {
    currentLang = 'de';
}

function applyTranslations() {
    // Falls das Übersetzungsobjekt komplett fehlt, breche sicher ab
    if (typeof translations === 'undefined' || !translations[currentLang]) return;

    document.documentElement.lang = currentLang;
    const langData = translations[currentLang];
    
    // Optimierung: Wir suchen alle relevanten Elemente in EINEM Durchgang
    document.querySelectorAll('[data-i18n], [data-i18n-placeholder]').forEach(el => {
        // 1. Normalen Text übersetzen
        if (el.hasAttribute('data-i18n')) {
            const key = el.getAttribute('data-i18n');
            if (langData[key] !== undefined) {
                if (el.tagName === 'TITLE') {
                    document.title = langData[key];
                } else {
                    el.innerText = langData[key];
                }
            }
        }
        
        // 2. Platzhalter für Input-Felder übersetzen
        if (el.hasAttribute('data-i18n-placeholder')) {
            const key = el.getAttribute('data-i18n-placeholder');
            if (langData[key] !== undefined) {
                el.setAttribute('placeholder', langData[key]);
            }
        }
    });

    // Dropdown-Auswahl visuell anpassen (wichtig für die automatische Browser-Erkennung)
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = currentLang;
    }
}

function changeLanguage(lang) {
    // Sicherstellen, dass das Objekt existiert, bevor wir darauf zugreifen
    if (typeof translations !== 'undefined' && translations[lang]) {
        currentLang = lang;
        localStorage.setItem('selectedLang', lang);
        applyTranslations();
    }
}

// Initialisierung sobald das HTML geladen ist
document.addEventListener('DOMContentLoaded', applyTranslations);

